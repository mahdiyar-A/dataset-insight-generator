from ai_engine.models.models import LLMReport


BRAND_DARK  = (15,  23,  42)
BRAND_MID   = (30,  41,  59)
BRAND_BLUE  = (59, 130, 246)
BRAND_TEXT  = (255, 255, 255)
BRAND_MUTED = (148, 163, 184)
WHITE       = (255, 255, 255)
ACCENT_GOLD = (251, 191, 36)


def _s(text: str) -> str:
    """Sanitize any text to latin-1 safe for fpdf Helvetica."""
    if not text:
        return ""
    replacements = {
        "\u2014": "-", "\u2013": "-", "\u2012": "-",
        "\u2018": "'", "\u2019": "'",
        "\u201c": '"', "\u201d": '"',
        "\u2022": "-", "\u2023": "-",
        "\u2026": "...",
        "\u00b7": "-",
        "\u2192": "->", "\u2190": "<-",
        "\u2191": "^",  "\u2193": "v",
        "\u21d2": "=>",
        "\u26a0": "!",
        "\u2713": "OK", "\u2714": "OK",
        "\u2717": "X",  "\u2718": "X",
    }
    for char, replacement in replacements.items():
        text = text.replace(char, replacement)
    return text.encode("latin-1", errors="replace").decode("latin-1")


class DigReport(FPDF):
    def __init__(self):
        super().__init__(orientation="P", unit="mm", format="A4")
        self.set_auto_page_break(auto=True, margin=20)
        self.set_margins(20, 20, 20)

    def header(self):
        # Paint full page background first
        self.set_fill_color(*BRAND_DARK)
        self.rect(0, 0, 210, 297, "F")
        # Top bar
        self.set_fill_color(*BRAND_MID)
        self.rect(0, 0, 210, 14, "F")
        self.set_y(3)
        self.set_font("Helvetica", "B", 9)
        self.set_text_color(*BRAND_BLUE)
        self.cell(0, 8, "DIG  |  Dataset Insight Generator", align="L")
        self.set_y(20)

    def footer(self):
        self.set_y(-13)
        self.set_font("Helvetica", "", 7)
        self.set_text_color(*BRAND_MUTED)
        self.cell(0, 8, f"Page {self.page_no()}  |  Generated by DIG AI Engine", align="C")

    def section_title(self, text: str):
        self.ln(4)
        self.set_font("Helvetica", "B", 13)
        self.set_text_color(*BRAND_BLUE)
        self.cell(0, 8, _s(text), ln=True)
        y = self.get_y()
        self.set_draw_color(*BRAND_BLUE)
        self.set_line_width(0.4)
        self.line(20, y, 190, y)
        self.ln(3)

    def body_text(self, text: str):
        self.set_font("Helvetica", "", 10)
        self.set_text_color(*BRAND_TEXT)
        self.multi_cell(0, 6, _s(text))
        self.ln(2)

    def muted_text(self, text: str):
        self.set_font("Helvetica", "I", 9)
        self.set_text_color(*BRAND_MUTED)
        self.multi_cell(0, 5, _s(text))
        self.ln(1)

    def insight_block(self, rank: int, title: str, body: str):
        self.set_fill_color(*BRAND_MID)
        self.set_draw_color(*BRAND_BLUE)
        self.set_line_width(0.3)
        self.rect(20, self.get_y(), 170, 7 + self._estimate_text_height(body, 160) + 6, "DF")

        y_start = self.get_y() + 2
        self.set_xy(22, y_start)
        self.set_font("Helvetica", "B", 8)
        # FIX: ACCENT_GOLD is a tuple, not a function
        self.set_text_color(*ACCENT_GOLD if rank == 1 else BRAND_MUTED)
        self.cell(12, 5, f"#{rank}", ln=False)

        self.set_font("Helvetica", "B", 11)
        self.set_text_color(*BRAND_TEXT)
        self.multi_cell(155, 6, _s(title))

        self.set_x(22)
        self.set_font("Helvetica", "", 10)
        self.set_text_color(180, 190, 205)
        self.multi_cell(165, 5.5, _s(body))
        self.ln(4)

    def _estimate_text_height(self, text: str, width_mm: float) -> float:
        chars_per_line = int(width_mm / 2.1)
        lines = max(1, len(text) // chars_per_line + text.count("\n"))
        return lines * 5.5

    def embed_chart(self, img_b64: str, title: str, desc: str):
        try:
            img_bytes = base64.b64decode(img_b64)
            with tempfile.NamedTemporaryFile(suffix=".png", delete=False) as tmp:
                tmp.write(img_bytes)
                tmp_path = tmp.name

            if self.get_y() > 220:
                self.add_page()

            self.set_font("Helvetica", "B", 9)
            self.set_text_color(*BRAND_MUTED)
            self.cell(0, 5, _s(f">> {title}"), ln=True)
            self.ln(1)

            img_w = 160
            self.image(tmp_path, x=25, w=img_w)
            self.ln(2)

            self.set_font("Helvetica", "I", 8)
            self.set_text_color(*BRAND_MUTED)
            self.set_x(25)
            self.multi_cell(160, 4.5, _s(desc))
            self.ln(5)

            os.unlink(tmp_path)
        except Exception as e:
            print(f"[PDF] Failed to embed chart '{title}': {e}")


def build_pdf(
    report: LLMReport,
    charts: List[Dict[str, Any]],
    stats_summary: Dict,
    was_cleaned: bool,
    file_name: str,
) -> str:
    pdf = DigReport()
    pdf.add_page()

    # Cover
    pdf.set_fill_color(*BRAND_DARK)
    pdf.rect(0, 0, 210, 297, "F")

    pdf.set_y(60)
    pdf.set_font("Helvetica", "B", 28)
    pdf.set_text_color(*BRAND_BLUE)
    pdf.cell(0, 14, "DATASET INSIGHT REPORT", align="C", ln=True)

    pdf.ln(4)
    pdf.set_font("Helvetica", "", 13)
    pdf.set_text_color(*BRAND_MUTED)
    pdf.cell(0, 8, _s(file_name), align="C", ln=True)

    pdf.ln(8)
    pdf.set_font("Helvetica", "", 10)
    pdf.set_text_color(*BRAND_MUTED)
    rows = stats_summary.get("rowCount", "?")
    cols = stats_summary.get("columnCount", "?")
    pdf.cell(0, 6, f"{rows} rows  |  {cols} columns  |  {'Cleaned' if was_cleaned else 'Original'} dataset", align="C", ln=True)

    pdf.ln(6)
    pdf.set_font("Helvetica", "I", 9)
    pdf.set_text_color(71, 85, 105)
    pdf.cell(0, 5, "Generated by DIG AI Engine  |  Powered by Gemini", align="C", ln=True)

    # Executive Summary
    pdf.add_page()
    pdf.section_title("Executive Summary")
    pdf.body_text(report.executiveSummary)

    if report.confidenceNote:
        pdf.ln(2)
        pdf.set_fill_color(30, 41, 59)
        pdf.set_draw_color(251, 191, 36)
        pdf.set_line_width(0.4)
        h = 8 + max(1, len(report.confidenceNote) // 80) * 5
        # FIX: check page boundary before drawing box
        if pdf.get_y() + h > 270:
            pdf.add_page()
        pdf.rect(20, pdf.get_y(), 170, h, "DF")
        pdf.set_xy(24, pdf.get_y() + 3)
        pdf.set_font("Helvetica", "B", 9)
        pdf.set_text_color(*ACCENT_GOLD)
        pdf.cell(0, 5, "! Analyst Confidence Note", ln=True)
        pdf.set_x(24)
        pdf.set_font("Helvetica", "I", 9)
        pdf.set_text_color(200, 200, 200)
        pdf.multi_cell(162, 5, _s(report.confidenceNote))
        pdf.ln(6)

    # Insights + Charts
    pdf.section_title("Key Insights")

    charts_by_insight: Dict[int, List] = {}
    for ch in charts:
        idx = ch.get("insight_index", 0)
        charts_by_insight.setdefault(idx, []).append(ch)

    for ins in report.insights:
        rank    = ins.get("rank", 0)
        title   = ins.get("title", "")
        body    = ins.get("body", "")
        ins_idx = ins.get("insight_index", rank)

        pdf.insight_block(rank, title, body)

        for ch in charts_by_insight.get(ins_idx, []):
            img = ch.get("image_base64")
            if img:
                pdf.embed_chart(img, ch.get("label", ""), ch.get("desc", ""))

    unmatched = charts_by_insight.get(0, [])
    if unmatched:
        pdf.section_title("Additional Visualizations")
        for ch in unmatched:
            img = ch.get("image_base64")
            if img:
                pdf.embed_chart(img, ch.get("label", ""), ch.get("desc", ""))

    # Data Quality
    pdf.add_page()
    pdf.section_title("Data Quality & Methodology")

    issues = stats_summary.get("detectedIssues", [])
    if issues:
        pdf.set_font("Helvetica", "B", 10)
        pdf.set_text_color(*BRAND_TEXT)
        pdf.cell(0, 6, "Issues detected in original dataset:", ln=True)
        pdf.ln(1)
        for issue in issues:
            pdf.set_font("Helvetica", "", 9)
            pdf.set_text_color(*BRAND_MUTED)
            pdf.cell(6, 5, "-", ln=False)
            pdf.multi_cell(160, 5, _s(issue))
    else:
        pdf.body_text("No significant data quality issues were detected.")

    if was_cleaned:
        pdf.ln(3)
        pdf.body_text(
            "The dataset was automatically cleaned prior to analysis: missing values were "
            "imputed using column medians (numeric) and mode (categorical), duplicate rows "
            "were removed, and extreme outliers were capped using the IQR method."
        )

    pdf_bytes = pdf.output(dest="S")
    if isinstance(pdf_bytes, str):
        pdf_bytes = pdf_bytes.encode("latin-1")
    return base64.b64encode(pdf_bytes).decode("utf-8")